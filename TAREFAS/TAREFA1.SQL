CREATE DATABASE LOJA;

USE LOJA;

CREATE TABLE CLIENTE (
	CPF BIGINT NOT NULL,
	NOME_CLI VARCHAR(255) NOT NULL,
	DATANASC DATE,
	ENDERECO VARCHAR(255),
	CIDADE VARCHAR(255),
	PRIMARY KEY (CPF)
);

CREATE TABLE CATEGORIA (
	ID_CATEGORIA SMALLINT NOT NULL AUTO_INCREMENT,
	TIPO_CATEG VARCHAR(255) NOT NULL,
	PRIMARY KEY (ID_CATEGORIA)
);

CREATE TABLE PRODUTO (
	ID_PROD SMALLINT NOT NULL AUTO_INCREMENT,
	ID_CATEGORIA SMALLINT,
	NOME_PRODUTO VARCHAR(255) NOT NULL,
	PRECO FLOAT NOT NULL, 
	QTD_ESTOQUE INT DEFAULT '100',
	PRIMARY KEY (ID_PROD),
	CONSTRAINT FK_PRODUTO_CATEGORIA FOREIGN KEY (ID_CATEGORIA) REFERENCES CATEGORIA(ID_CATEGORIA)
);

CREATE TABLE PEDIDO (
	ID_PEDIDO SMALLINT NOT NULL AUTO_INCREMENT,
	ID_PROD SMALLINT,
	CPF BIGINT,
	DATA_PEDIDO DATE,
	STATUS_PEDIDO VARCHAR (2),
	PRIMARY KEY (ID_PEDIDO),
	CONSTRAINT FK_PEDIDO_PRODUTO FOREIGN KEY (ID_PROD) REFERENCES PRODUTO(ID_PROD),
	CONSTRAINT FK_PEDIDO_CLIENTE FOREIGN KEY (CPF) REFERENCES CLIENTE(CPF)
);


